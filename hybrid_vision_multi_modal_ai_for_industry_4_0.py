    dummy_input = np.zeros((1, X.shape[1]))
    # (Optional: Map specific inputs to their correct column index)

    predicted_rul = model_ml.predict(dummy_input)[0]

    # 2. DL Prediction
    img = Image.fromarray(input_img).resize((224, 224))
    img_array = np.array(img) / 255.0
    img_array = np.expand_dims(img_array, axis=0)

    prediction = model_dl.predict(img_array)
    defect_type = class_names[np.argmax(prediction)]
    confidence = float(np.max(prediction))

    # 3. Decision Logic for the UI
    status = "✅ OPERATIONAL" if predicted_rul > 50 else "⚠️ MAINTENANCE REQUIRED"

    return status, f"{int(predicted_rul)} Cycles", f"{defect_type} ({confidence:.2%})"

