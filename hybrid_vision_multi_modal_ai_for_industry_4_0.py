import pandas as pd
import os
import glob

# 1. Find the file anywhere inside the path_ml directory
search_pattern = os.path.join(path_ml, "**", "train_FD001.txt")
found_files = glob.glob(search_pattern, recursive=True)

if not found_files:
    print("❌ Still can't find it. Let's see what IS in there:")
    print(os.listdir(path_ml))
else:
    file_path = found_files[0]
    print(f"✅ Found it at: {file_path}")

    # 2. Load the data (using r'\s+' to fix the SyntaxWarning)
    train_df = pd.read_csv(file_path, sep=r'\s+', header=None)

    # 3. Name the columns
    columns = ['unit_nr', 'cycles', 'os_1', 'os_2', 'os_3'] + [f's_{i}' for i in range(1, 22)]
    train_df.columns = columns

    # 4. Calculate RUL (Remaining Useful Life)
    max_cycle = train_df.groupby('unit_nr')['cycles'].max().reset_index()
    max_cycle.columns = ['unit_nr', 'max_cycles']
    train_df = train_df.merge(max_cycle, on='unit_nr', how='left')
    train_df['RUL'] = train_df['max_cycles'] - train_df['cycles']
    train_df.drop('max_cycles', axis=1, inplace=True)

    print("📊 Data Loaded Successfully!")
    print(train_df[['unit_nr', 'cycles', 'RUL']].head())

# Identify sensors with zero variance (they never change)
dead_sensors = [col for col in train_df.columns if train_df[col].nunique() <= 1]
print(f"Dropping useless sensors: {dead_sensors}")

# Keep only useful features
train_df.drop(columns=dead_sensors, inplace=True)
print(f"Remaining useful columns: {list(train_df.columns)}")

